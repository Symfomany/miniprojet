/**
 *  Classe abstraite est une Classe
 *  qui sert de modele aux autres classes
 *  Une classe abstraite ne peut etre hérité d'une autre classe
 */
abstract class Humain{

  /**
   *
   * @var [type]
   */
  protected $nom;

  /**
   * [$prenom description]
   * @var [type]
   */
  protected $prenom;






}







